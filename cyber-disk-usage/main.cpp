#include <QGuiApplication>
#include <QQmlApplicationEngine>
#include <QQmlContext>

#include "backend.h"

int main(int argc, char *argv[])
{
    QCoreApplication::setAttribute(Qt::AA_EnableHighDpiScaling);
    QGuiApplication app(argc, argv);

    qmlRegisterType<Backend>("org.cyber.DiskUsage", 1, 0, "Backend");
    qmlRegisterType<Drive>("org.cyber.DiskUsage", 1, 0, "Drive");

    QQmlApplicationEngine engine;

    #ifdef QT_DEBUG
    engine.rootContext()->setContextProperty(QStringLiteral("debug"), true);
    #else
    engine.rootContext()->setContextProperty(QStringLiteral("debug"), false);
    #endif

    engine.addImportPath(QStringLiteral("qrc:/"));
    engine.load(QUrl(QStringLiteral("qrc:/qml/main.qml")));

    return app.exec();
}
